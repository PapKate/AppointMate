namespace MeetBase.Blazor
{
    /// <summary>
    /// The CSS property text-align
    /// </summary>
    public enum TextAlign
    {
        /// <summary>
        /// Positions the text on the left
        /// </summary>
        Left,

        /// <summary>
        /// Positions the text on the center
        /// </summary>
        Center,

        /// <summary>
        /// Positions the text on the right
        /// </summary>
        Right,

        /// <summary>
        /// Stretches the lines so that each line has equal width (like in newspapers and magazines)
        /// </summary>
        Justify,

        /// <summary>
        /// Sets this property to its default value.
        /// </summary>
        Initial,

        /// <summary>
        /// Inherits this property from its parent element.
        /// </summary>
        Inherit
    }
}
