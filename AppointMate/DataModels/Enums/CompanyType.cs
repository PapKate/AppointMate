namespace AppointMate
{
    /// <summary>
    /// Provides enumeration over the company types
    /// </summary>
    public enum CompanyType
    {
        /// <summary>
        /// Companies in this category focus on developing and providing software, hardware, and technology-related services.
        /// This includes software development companies, IT consulting firms, cybersecurity providers, and cloud computing services.
        /// </summary>
        InformationTechnology,

        /// <summary>
        /// This category encompasses companies that specifically focus on software development and technology solutions,
        /// which may not be limited to traditional IT services. It includes companies developing applications, 
        /// mobile apps, gaming software, and emerging technologies like artificial intelligence and virtual reality.
        /// </summary>
        SoftwareAndTechnology,

        /// <summary>
        /// Companies in the finance sector include banks, credit unions, investment firms, insurance companies, and other financial institutions. 
        /// They provide financial services such as banking, investment management, lending, and insurance.
        /// </summary>
        FinanceAndBanking,

        /// <summary>
        /// This category covers companies in the food and beverage industry, specifically those involved in coffee production, distribution, and retail. 
        /// It includes coffee roasters, cafes, coffeehouse chains, and beverage manufacturers.
        /// </summary>
        CoffeeAndBeverage,

        /// <summary>
        ///  Companies in this category focus on providing healthcare services, pharmaceutical products, medical equipment, and healthcare technology solutions.
        /// </summary>
        HealthcareAndPharmaceuticals,

        /// <summary>
        /// Companies in this sector are involved in construction projects, infrastructure development, and engineering services.
        /// </summary>
        ConstructionAndEngineering
    }
}
